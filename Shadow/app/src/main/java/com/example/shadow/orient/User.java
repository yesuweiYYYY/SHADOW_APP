package com.example.shadow.orient;

public class User {
    private String username;
    private String password;
    private String sex;
    private String age;
    private String phone;
    private String descrption;
    private String image;
    public User(String username,
            String password,
            String sex,
            String age,
            String phone,
            String descrption,
            String image){

        this.username = username;
        this.username = password;
        this.username = sex;
        this.username = age;
        this.username = phone;
        this.username = descrption;
        this.username = image;

    }

}
